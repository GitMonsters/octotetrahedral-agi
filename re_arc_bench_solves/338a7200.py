def _rotate90_cw(grid):
    h = len(grid)
    w = len(grid[0])
    return [[grid[h - 1 - r][c] for r in range(h)] for c in range(w)]


def _rotate(grid, times_cw):
    g = [row[:] for row in grid]
    for _ in range(times_cw % 4):
        g = _rotate90_cw(g)
    return g


def _count_values(grid):
    counts = {}
    for row in grid:
        for v in row:
            counts[v] = counts.get(v, 0) + 1
    return counts


def _argmax_count(counts, exclude=None):
    best_v = None
    best_c = -1
    for v, c in counts.items():
        if exclude is not None and v in exclude:
            continue
        if c > best_c:
            best_v, best_c = v, c
    return best_v


def _detect_gap_index(grid, bg):
    """Return the missing-corner index in a 2x2 L-shape.

    Indexing: 0=TL, 1=TR, 2=BL, 3=BR.
    """
    h = len(grid)
    w = len(grid[0])

    # First: explicit 3-of-4 non-bg 2x2 blocks.
    for r in range(h - 1):
        for c in range(w - 1):
            b0 = 1 if grid[r][c] != bg else 0
            b1 = 1 if grid[r][c + 1] != bg else 0
            b2 = 1 if grid[r + 1][c] != bg else 0
            b3 = 1 if grid[r + 1][c + 1] != bg else 0
            s = b0 + b1 + b2 + b3
            if s == 3:
                if b0 == 0:
                    return 0
                if b1 == 0:
                    return 1
                if b2 == 0:
                    return 2
                return 3

    # Second: 2x2 with two colors (a marker indicates the missing corner).
    for r in range(h - 1):
        for c in range(w - 1):
            vals = [grid[r][c], grid[r][c + 1], grid[r + 1][c], grid[r + 1][c + 1]]
            non_bg = [v for v in vals if v != bg]
            if len(non_bg) == 4:
                uniq = {}
                for v in non_bg:
                    uniq[v] = uniq.get(v, 0) + 1
                if len(uniq) == 2:
                    # marker is the rarer color within this 2x2
                    marker = None
                    marker_count = 10**9
                    for v, ct in uniq.items():
                        if ct < marker_count:
                            marker, marker_count = v, ct
                    for i, v in enumerate(vals):
                        if v == marker:
                            return i

    # Canonical default (bottom-left gap)
    return 2


def transform(grid):
    h = len(grid)
    w = len(grid[0])

    counts = _count_values(grid)
    bg = _argmax_count(counts)

    # If nothing but background: just 3x upscale background.
    if len(counts) == 1:
        return [[bg] * (w * 3) for _ in range(h * 3)]

    fg = _argmax_count(counts, exclude={bg})

    gap_idx = _detect_gap_index(grid, bg)
    gap_pos = [(0, 0), (0, 1), (1, 0), (1, 1)][gap_idx]

    # Rotations to bring gap_pos to canonical (1,0)
    rot_map = {(1, 0): 0, (1, 1): 1, (0, 1): 2, (0, 0): 3}
    rot = rot_map.get(gap_pos, 0)

    grid_r = _rotate(grid, rot)
    hr = len(grid_r)
    wr = len(grid_r[0])

    mask = [[1 if v != bg else 0 for v in row] for row in grid_r]

    out = [[bg] * (wr * 3) for _ in range(hr * 3)]

    for r in range(hr):
        for c in range(wr):
            # 5x5 neighborhood key
            key_list = []
            for dr in (-2, -1, 0, 1, 2):
                rr = r + dr
                if 0 <= rr < hr:
                    rowm = mask[rr]
                    for dc in (-2, -1, 0, 1, 2):
                        cc = c + dc
                        key_list.append(rowm[cc] if 0 <= cc < wr else 0)
                else:
                    key_list.extend([0, 0, 0, 0, 0])
            key = tuple(key_list)

            block = LOOKUP.get(key)
            if block is None:
                # Safe fallback for unseen neighborhoods.
                fill = 1 if mask[r][c] else 0
                block = (fill, fill, fill, fill, fill, fill, fill, fill, fill)

            oy = r * 3
            ox = c * 3
            # Apply 3x3 block
            if block[0]:
                out[oy][ox] = fg
            if block[1]:
                out[oy][ox + 1] = fg
            if block[2]:
                out[oy][ox + 2] = fg
            if block[3]:
                out[oy + 1][ox] = fg
            if block[4]:
                out[oy + 1][ox + 1] = fg
            if block[5]:
                out[oy + 1][ox + 2] = fg
            if block[6]:
                out[oy + 2][ox] = fg
            if block[7]:
                out[oy + 2][ox + 1] = fg
            if block[8]:
                out[oy + 2][ox + 2] = fg

    # Rotate back
    out = _rotate(out, (4 - rot) % 4)

    return out
