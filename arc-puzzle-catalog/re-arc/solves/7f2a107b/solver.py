def _as_tuple(grid):
    return tuple(tuple(row) for row in grid)


def _repeat_box(grid):
    h = len(grid)
    w = len(grid[0])
    counts = {}
    for row in grid:
        for value in row:
            counts[value] = counts.get(value, 0) + 1
    bg = max(counts, key=counts.get)
    rows = [r for r in range(h) if any(grid[r][c] != bg for c in range(w))]
    cols = [c for c in range(w) if any(grid[r][c] != bg for r in range(h))]
    if not rows or not cols:
        return [row[:] + row[:] for row in grid for _ in (0, 1)]
    r0, r1 = rows[0], rows[-1]
    c0, c1 = cols[0], cols[-1]
    box = [row[c0:c1 + 1] for row in grid[r0:r1 + 1]]
    bh = len(box)
    bw = len(box[0])
    out_h = 2 * h
    out_w = 2 * w
    out = []
    for r in range(out_h):
        src_row = box[r % bh]
        out.append([src_row[c % bw] for c in range(out_w)])
    return out


