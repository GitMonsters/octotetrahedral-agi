def transform(input_grid):
    """
    Rule: Each colored shape (stamp) has blue(1) line markers indicating
    directions to tile the stamp pattern. The stamp tiles modularly from
    its position to each marker.
    """
    grid = input_grid
    rows = len(grid)
    cols = len(grid[0])

    # Collect stamp cells and blue cells
    stamp_cells_by_color = {}
    blue_cells = []
    for r in range(rows):
        for c in range(cols):
            v = grid[r][c]
            if v == 1:
                blue_cells.append((r, c))
            elif v != 0:
                stamp_cells_by_color.setdefault(v, []).append((r, c))

    # Find stamps via BFS connected components
    stamps = []
    for color, cells in stamp_cells_by_color.items():
        cell_set = set(cells)
        visited = set()
        for cell in cells:
            if cell in visited:
                continue
            component = []
            queue = [cell]
            visited.add(cell)
            while queue:
                cr, cc = queue.pop(0)
                component.append((cr, cc))
                for dr, dc in [(-1,0),(1,0),(0,-1),(0,1)]:
                    nr, nc = cr+dr, cc+dc
                    if (nr, nc) in cell_set and (nr, nc) not in visited:
                        visited.add((nr, nc))
                        queue.append((nr, nc))
            min_r = min(r for r, c in component)
            max_r = max(r for r, c in component)
            min_c = min(c for r, c in component)
            max_c = max(c for r, c in component)
            h = max_r - min_r + 1
            w = max_c - min_c + 1
            pattern = [[0]*w for _ in range(h)]
            for r2, c2 in component:
                pattern[r2 - min_r][c2 - min_c] = color
            stamps.append({
                'color': color, 'min_r': min_r, 'max_r': max_r,
                'min_c': min_c, 'max_c': max_c, 'h': h, 'w': w,
                'pattern': pattern
            })

    # Find blue markers via BFS
    markers = []
    blue_set = set(blue_cells)
    blue_visited = set()
    for cell in blue_cells:
        if cell in blue_visited:
            continue
        component = []
        queue = [cell]
        blue_visited.add(cell)
        while queue:
            cr, cc = queue.pop(0)
            component.append((cr, cc))
            for dr, dc in [(-1,0),(1,0),(0,-1),(0,1)]:
                nr, nc = cr+dr, cc+dc
                if (nr, nc) in blue_set and (nr, nc) not in blue_visited:
                    blue_visited.add((nr, nc))
                    queue.append((nr, nc))
        min_r = min(r for r, c in component)
        max_r = max(r for r, c in component)
        min_c = min(c for r, c in component)
        max_c = max(c for r, c in component)
        if min_r == max_r:
            markers.append({'type': 'horizontal', 'row': min_r,
                          'col_start': min_c, 'col_end': max_c})
        elif min_c == max_c:
            markers.append({'type': 'vertical', 'col': min_c,
                          'row_start': min_r, 'row_end': max_r})

    # Output: start from input, remove blue
    output = [row[:] for row in input_grid]
    for r, c in blue_cells:
        output[r][c] = 0

    # Tile each stamp in each marker's direction
    for marker in markers:
        if marker['type'] == 'vertical':
            mc = marker['col']
            for stamp in stamps:
                if stamp['min_r'] == marker['row_start'] and stamp['max_r'] == marker['row_end']:
                    if mc > stamp['max_c']:
                        fc_s, fc_e = stamp['min_c'], mc
                    else:
                        fc_s, fc_e = mc, stamp['max_c']
                    for r in range(stamp['min_r'], stamp['max_r'] + 1):
                        for c in range(fc_s, fc_e + 1):
                            pr = (r - stamp['min_r']) % stamp['h']
                            pc = (c - stamp['min_c']) % stamp['w']
                            output[r][c] = stamp['pattern'][pr][pc]
                    break
        else:  # horizontal
            mr = marker['row']
            for stamp in stamps:
                if stamp['min_c'] == marker['col_start'] and stamp['max_c'] == marker['col_end']:
                    if mr > stamp['max_r']:
                        fr_s, fr_e = stamp['min_r'], mr
                    else:
                        fr_s, fr_e = mr, stamp['max_r']
                    for r in range(fr_s, fr_e + 1):
                        for c in range(stamp['min_c'], stamp['max_c'] + 1):
                            pr = (r - stamp['min_r']) % stamp['h']
                            pc = (c - stamp['min_c']) % stamp['w']
                            output[r][c] = stamp['pattern'][pr][pc]
                    break

    return output


