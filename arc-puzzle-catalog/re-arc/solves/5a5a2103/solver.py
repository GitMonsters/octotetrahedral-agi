"""ARC puzzle 5a5a2103 solver.

Rule: Grid is divided by separator lines into cells. One cell contains a
"stencil" shape (in a unique color). Each row of cells has a 2x2 colored
block in the first column. Output: every cell gets the stencil pattern
filled with that row's color.
"""

def transform(input_grid: list[list[int]]) -> list[list[int]]:
    grid = [row[:] for row in input_grid]
    H, W = len(grid), len(grid[0])

    # Find separator color: color that fills a complete row
    sep_color = None
    for r in range(H):
        if len(set(grid[r])) == 1 and grid[r][0] != 0:
            sep_color = grid[r][0]
            break

    # Find separator rows and columns
    sep_rows = [r for r in range(H) if all(grid[r][c] == sep_color for c in range(W))]
    sep_cols = [c for c in range(W) if all(grid[r][c] == sep_color for r in range(H))]

    # Build cell bands (start, end) between separators
    def make_bands(seps, total):
        bands = []
        start = 0
        for s in seps:
            if s > start:
                bands.append((start, s))
            start = s + 1
        if start < total:
            bands.append((start, total))
        return bands

    row_bands = make_bands(sep_rows, H)
    col_bands = make_bands(sep_cols, W)
    cell_h = row_bands[0][1] - row_bands[0][0]
    cell_w = col_bands[0][1] - col_bands[0][0]

    def get_cell(rb, cb):
        r0, r1 = row_bands[rb]
        c0, c1 = col_bands[cb]
        return [[grid[r][c] for c in range(c0, c1)] for r in range(r0, r1)]

    def cell_nonzero_colors(cell):
        return {v for row in cell for v in row if v != 0}

    # Row colors from first-column cells
    row_colors = {}
    for rb in range(len(row_bands)):
        colors = cell_nonzero_colors(get_cell(rb, 0))
        if colors:
            row_colors[rb] = colors.pop()

    # Find stencil: cell with a color not in row_colors values
    rc_set = set(row_colors.values())
    stencil = None
    for rb in range(len(row_bands)):
        for cb in range(len(col_bands)):
            cell = get_cell(rb, cb)
            colors = cell_nonzero_colors(cell)
            if colors - rc_set:  # has a non-row-color
                stencil = [[1 if cell[r][c] != 0 else 0
                            for c in range(cell_w)] for r in range(cell_h)]
                break
        if stencil:
            break

    # Build output: fill every cell with stencil pattern in its row's color
    output = [row[:] for row in grid]
    for rb in range(len(row_bands)):
        color = row_colors.get(rb)
        if color is None:
            continue
        r0 = row_bands[rb][0]
        for cb in range(len(col_bands)):
            c0 = col_bands[cb][0]
            for r in range(cell_h):
                for c in range(cell_w):
                    output[r0 + r][c0 + c] = color if stencil[r][c] else 0
    return output


