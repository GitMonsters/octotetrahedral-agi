"""ARC puzzle 8731374e solver.
Rule: Find the large rectangular region filled mostly with one color (with a few
marker pixels of a different color). Extract it, then draw full horizontal and
vertical cross-hair lines through each marker pixel's position.
"""

def transform(input_grid: list[list[int]]) -> list[list[int]]:
    rows = len(input_grid)
    cols = len(input_grid[0])

    # Build prefix sums for each color to quickly count in any rectangle
    prefix = {}
    for c in range(10):
        ps = [[0] * (cols + 1) for _ in range(rows + 1)]
        for r in range(rows):
            for cc in range(cols):
                ps[r + 1][cc + 1] = (1 if input_grid[r][cc] == c else 0) + ps[r][cc + 1] + ps[r + 1][cc] - ps[r][cc]
        prefix[c] = ps

    def rect_count(ps, r1, c1, r2, c2):
        return ps[r2 + 1][c2 + 1] - ps[r1][c2 + 1] - ps[r2 + 1][c1] + ps[r1][c1]

    # Find the largest rectangle that is almost entirely one color (with 1-5 markers)
    best_area = 0
    best = None
    for c in range(10):
        ps = prefix[c]
        for r1 in range(rows):
            for r2 in range(r1 + 3, rows):
                h = r2 - r1 + 1
                for c1 in range(cols):
                    for c2 in range(c1 + 3, cols):
                        w = c2 - c1 + 1
                        area = h * w
                        if area <= best_area:
                            continue
                        count = rect_count(ps, r1, c1, r2, c2)
                        markers = area - count
                        if 0 < markers <= min(5, max(1, area // 20)):
                            best = (c, r1, r2, c1, c2)
                            best_area = area
    
    region_color, r1, r2, c1, c2 = best
    h = r2 - r1 + 1
    w = c2 - c1 + 1

    # Find marker positions and color
    marker_rows = set()
    marker_cols = set()
    marker_color = None
    for r in range(h):
        for cc in range(w):
            val = input_grid[r1 + r][c1 + cc]
            if val != region_color:
                marker_color = val
                marker_rows.add(r)
                marker_cols.add(cc)

    # Build output: fill with region_color, draw cross-hair lines
    output = [[region_color] * w for _ in range(h)]
    for r in marker_rows:
        for cc in range(w):
            output[r][cc] = marker_color
    for cc in marker_cols:
        for r in range(h):
            output[r][cc] = marker_color

    return output


