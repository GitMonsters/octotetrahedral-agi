def transform(input_grid: list[list[int]]) -> list[list[int]]:
    rows = len(input_grid)
    cols = len(input_grid[0])

    # Find separator color and separator rows (uniform non-zero rows)
    sep_color = None
    sep_rows = []
    for r in range(rows):
        if len(set(input_grid[r])) == 1 and input_grid[r][0] != 0:
            if sep_color is None:
                sep_color = input_grid[r][0]
            sep_rows.append(r)

    # Find separator columns (all cells = sep_color)
    sep_cols = []
    for c in range(cols):
        if all(input_grid[r][c] == sep_color for r in range(rows)):
            sep_cols.append(c)

    # Get block ranges between separators
    def get_ranges(seps, total):
        ranges, prev = [], 0
        for s in seps:
            if s > prev:
                ranges.append((prev, s - 1))
            prev = s + 1
        if prev < total:
            ranges.append((prev, total - 1))
        return ranges

    block_row_ranges = get_ranges(sep_rows, rows)
    block_col_ranges = get_ranges(sep_cols, cols)

    # Build block grid: each cell = color of corresponding block
    block_grid = []
    for br_start, _ in block_row_ranges:
        row = []
        for bc_start, _ in block_col_ranges:
            row.append(input_grid[br_start][bc_start])
        block_grid.append(row)

    # Horizontal flip (reverse each row)
    return [list(reversed(row)) for row in block_grid]


