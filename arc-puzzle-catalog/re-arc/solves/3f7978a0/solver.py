def transform(input_grid: list[list[int]]) -> list[list[int]]:
    """Extract the rectangular frame defined by gray(5) bars, expanded by 1 row top/bottom."""
    # Find bounding box of all gray(5) cells
    rows_5, cols_5 = [], []
    for r, row in enumerate(input_grid):
        for c, val in enumerate(row):
            if val == 5:
                rows_5.append(r)
                cols_5.append(c)
    min_r, max_r = min(rows_5) - 1, max(rows_5) + 1
    min_c, max_c = min(cols_5), max(cols_5)
    return [row[min_c:max_c+1] for row in input_grid[min_r:max_r+1]]

