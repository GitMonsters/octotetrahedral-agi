"""ARC puzzle e9c9d9a1 solver.

Rule: Green(3) lines form a grid of rectangular regions.
- Top-left corner region  → Red(2)
- Top-right corner region → Yellow(4)
- Bottom-left corner      → Blue(1)
- Bottom-right corner     → Cyan(8)
- Interior regions (not on any edge) → Orange(7)
- Edge (non-corner) regions stay Black(0)
"""

def transform(input_grid: list[list[int]]) -> list[list[int]]:
    import copy
    rows = len(input_grid)
    cols = len(input_grid[0])
    output = copy.deepcopy(input_grid)

    # Find horizontal separator rows (all 3)
    h_lines = [r for r in range(rows) if all(input_grid[r][c] == 3 for c in range(cols))]
    # Find vertical separator columns (all 3)
    v_lines = [c for c in range(cols) if all(input_grid[r][c] == 3 for r in range(rows))]

    # Build row bands (ranges between horizontal lines)
    row_bounds = []
    prev = 0
    for h in h_lines:
        if h > prev:
            row_bounds.append((prev, h - 1))
        prev = h + 1
    if prev < rows:
        row_bounds.append((prev, rows - 1))

    # Build col bands
    col_bounds = []
    prev = 0
    for v in v_lines:
        if v > prev:
            col_bounds.append((prev, v - 1))
        prev = v + 1
    if prev < cols:
        col_bounds.append((prev, cols - 1))

    num_rb = len(row_bounds)
    num_cb = len(col_bounds)

    for ri, (r0, r1) in enumerate(row_bounds):
        for ci, (c0, c1) in enumerate(col_bounds):
            is_top = (ri == 0)
            is_bot = (ri == num_rb - 1)
            is_left = (ci == 0)
            is_right = (ci == num_cb - 1)

            if is_top and is_left:
                color = 2   # Red
            elif is_top and is_right:
                color = 4   # Yellow
            elif is_bot and is_left:
                color = 1   # Blue
            elif is_bot and is_right:
                color = 8   # Cyan
            elif not is_top and not is_bot and not is_left and not is_right:
                color = 7   # Orange
            else:
                color = 0   # stays black

            for r in range(r0, r1 + 1):
                for c in range(c0, c1 + 1):
                    if output[r][c] == 0:
                        output[r][c] = color

    return output


