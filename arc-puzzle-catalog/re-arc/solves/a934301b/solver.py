"""ARC-AGI puzzle a934301b solver.

Rule: Find connected components of non-zero cells. Keep only those with
≤1 cyan(8) cells. Remove (zero out) components with 2+ cyan(8) cells.
"""
from collections import deque

def transform(input_grid):
    rows = len(input_grid)
    cols = len(input_grid[0])
    visited = [[False]*cols for _ in range(rows)]

    def bfs(sr, sc):
        q = deque([(sr, sc)])
        visited[sr][sc] = True
        cells = [(sr, sc)]
        while q:
            r, c = q.popleft()
            for dr, dc in [(-1,0),(1,0),(0,-1),(0,1)]:
                nr, nc = r+dr, c+dc
                if 0 <= nr < rows and 0 <= nc < cols and not visited[nr][nc] and input_grid[nr][nc] != 0:
                    visited[nr][nc] = True
                    q.append((nr, nc))
                    cells.append((nr, nc))
        return cells

    output = [[0]*cols for _ in range(rows)]

    for r in range(rows):
        for c in range(cols):
            if input_grid[r][c] != 0 and not visited[r][c]:
                component = bfs(r, c)
                cyan_count = sum(1 for cr, cc in component if input_grid[cr][cc] == 8)
                if cyan_count <= 1:
                    for cr, cc in component:
                        output[cr][cc] = input_grid[cr][cc]

    return output


