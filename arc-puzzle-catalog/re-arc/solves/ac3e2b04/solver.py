"""ARC puzzle ac3e2b04 solver.

Rule: Each green 3x3 box (green border, red center) sits on a red line.
A blue perpendicular line is drawn through the box center across the grid.
At every crossing of that line with another red line, a blue 3x3 copy is stamped.
"""
import copy

def transform(input_grid: list[list[int]]) -> list[list[int]]:
    grid = copy.deepcopy(input_grid)
    rows = len(grid)
    cols = len(grid[0])

    # Find green 3x3 box centers (center=2, surrounded by 3s)
    green_centers = []
    for r in range(1, rows - 1):
        for c in range(1, cols - 1):
            if (grid[r][c] == 2 and
                all(grid[r+dr][c+dc] == 3
                    for dr in (-1, 0, 1) for dc in (-1, 0, 1)
                    if not (dr == 0 and dc == 0))):
                green_centers.append((r, c))

    # Identify red lines: rows/cols with no 0s
    h_lines = {r for r in range(rows) if all(grid[r][c] != 0 for c in range(cols))}
    v_lines = {c for c in range(cols) if all(grid[r][c] != 0 for r in range(rows))}

    for cr, cc in green_centers:
        if cr in h_lines:
            # Box on horizontal red line → perpendicular is vertical (col cc)
            for r in range(rows):
                if grid[r][cc] == 0:
                    grid[r][cc] = 1
            for hr in h_lines:
                if hr == cr:
                    continue
                for dr in (-1, 0, 1):
                    for dc in (-1, 0, 1):
                        nr, nc = hr + dr, cc + dc
                        if 0 <= nr < rows and 0 <= nc < cols:
                            if not (dr == 0 and dc == 0) and grid[nr][nc] != 3:
                                grid[nr][nc] = 1
        elif cc in v_lines:
            # Box on vertical red line → perpendicular is horizontal (row cr)
            for c in range(cols):
                if grid[cr][c] == 0:
                    grid[cr][c] = 1
            for vc in v_lines:
                if vc == cc:
                    continue
                for dr in (-1, 0, 1):
                    for dc in (-1, 0, 1):
                        nr, nc = cr + dr, vc + dc
                        if 0 <= nr < rows and 0 <= nc < cols:
                            if not (dr == 0 and dc == 0) and grid[nr][nc] != 3:
                                grid[nr][nc] = 1
    return grid


