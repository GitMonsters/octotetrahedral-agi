"""
ARC Puzzle 3a301edc

Rule: Two concentric non-black rectangles (outer frame color A, inner color B).
Add a new outer ring of color B around A.
Ring width = min(inner_w, inner_h), capped by available grid space.
"""
import json
from typing import List

Grid = List[List[int]]


def transform(input_grid: Grid) -> Grid:
    rows = len(input_grid)
    cols = len(input_grid[0])
    
    # Find non-black colors and their bounding boxes
    colors = {}
    for r in range(rows):
        for c in range(cols):
            v = input_grid[r][c]
            if v != 0:
                if v not in colors:
                    colors[v] = [r, c, r, c]  # min_r, min_c, max_r, max_c
                else:
                    colors[v][0] = min(colors[v][0], r)
                    colors[v][1] = min(colors[v][1], c)
                    colors[v][2] = max(colors[v][2], r)
                    colors[v][3] = max(colors[v][3], c)
    
    assert len(colors) == 2, f"Expected 2 non-black colors, got {len(colors)}"
    
    c1, c2 = list(colors.keys())
    bb1, bb2 = colors[c1], colors[c2]
    
    # Determine which is outer (larger bbox) and which is inner
    def bbox_area(bb):
        return (bb[2] - bb[0] + 1) * (bb[3] - bb[1] + 1)
    
    if bbox_area(bb1) > bbox_area(bb2):
        outer_color, inner_color = c1, c2
        outer_bb, inner_bb = bb1, bb2
    else:
        outer_color, inner_color = c2, c1
        outer_bb, inner_bb = bb2, bb1
    
    inner_w = inner_bb[3] - inner_bb[1] + 1
    inner_h = inner_bb[2] - inner_bb[0] + 1
    
    # Available space around outer rectangle
    top_avail = outer_bb[0]
    bottom_avail = rows - 1 - outer_bb[2]
    left_avail = outer_bb[1]
    right_avail = cols - 1 - outer_bb[3]
    
    # New ring width
    b2 = min(inner_w, inner_h, top_avail, bottom_avail, left_avail, right_avail)
    
    # Build output (copy input)
    output = [row[:] for row in input_grid]
    
    # New ring extends b2 cells on each side of the outer rectangle
    new_top = outer_bb[0] - b2
    new_bot = outer_bb[2] + b2
    new_left = outer_bb[1] - b2
    new_right = outer_bb[3] + b2
    
    for r in range(new_top, new_bot + 1):
        for c in range(new_left, new_right + 1):
            if 0 <= r < rows and 0 <= c < cols:
                # Only paint if currently black (don't overwrite existing)
                if output[r][c] == 0:
                    output[r][c] = inner_color
    
    return output


