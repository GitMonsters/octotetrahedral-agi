import copy

def transform(input_grid: list[list[int]]) -> list[list[int]]:
    grid = copy.deepcopy(input_grid)
    rows = len(grid)
    cols = len(grid[0])

    # Find full black rows and columns (dividers)
    h_lines = [r for r in range(rows) if all(grid[r][c] == 0 for c in range(cols))]
    v_lines = [c for c in range(cols) if all(grid[r][c] == 0 for r in range(rows))]

    # Build section boundaries: list of (start_row, end_row) for row sections
    row_bounds = []
    prev = 0
    for hl in h_lines:
        if hl > prev:
            row_bounds.append((prev, hl - 1))
        prev = hl + 1
    if prev < rows:
        row_bounds.append((prev, rows - 1))

    col_bounds = []
    prev = 0
    for vl in v_lines:
        if vl > prev:
            col_bounds.append((prev, vl - 1))
        prev = vl + 1
    if prev < cols:
        col_bounds.append((prev, cols - 1))

    # Find the 9 position (center of gray block) and the 6 position (magenta)
    pos_9 = None
    pos_6 = None
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] == 9:
                pos_9 = (r, c)
            elif grid[r][c] == 6:
                pos_6 = (r, c)

    # Find which section each is in
    def get_section(r, c):
        sr, sc = -1, -1
        for i, (r0, r1) in enumerate(row_bounds):
            if r0 <= r <= r1:
                sr = i
                break
        for j, (c0, c1) in enumerate(col_bounds):
            if c0 <= c <= c1:
                sc = j
                break
        return sr, sc

    sec_9 = get_section(*pos_9)
    sec_6 = get_section(*pos_6)

    # Direction: sign of (section_6 - section_9)
    def sign(x):
        return (x > 0) - (x < 0)

    dr = sign(sec_6[0] - sec_9[0])
    dc = sign(sec_6[1] - sec_9[1])

    # Move 9 within gray block
    nr, nc = pos_9[0] + dr, pos_9[1] + dc
    grid[pos_9[0]][pos_9[1]] = 5  # replace old 9 with gray
    grid[nr][nc] = 9  # place 9 in new position

    # Replace magenta 6 with 9
    grid[pos_6[0]][pos_6[1]] = 9

    return grid


