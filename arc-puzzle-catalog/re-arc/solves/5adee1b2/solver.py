"""ARC puzzle 5adee1b2 solver.

Rule: Bottom-left corner contains legend pairs (2-row blocks at cols 0-1) mapping
shape_color → border_color. Each shape gets a 1-pixel border (expanded bbox) filled
with its paired border_color. Interior holes (enclosed 0s) stay as 0.
"""

from collections import deque
import copy


def transform(input_grid: list[list[int]]) -> list[list[int]]:
    rows = len(input_grid)
    cols = len(input_grid[0])
    output = copy.deepcopy(input_grid)

    # Find legend pairs at columns 0-1 (2-row vertical blocks)
    legend: dict[int, int] = {}
    legend_cells: set[tuple[int, int]] = set()

    r = 0
    while r < rows - 1:
        if (input_grid[r][0] != 0 and input_grid[r][1] != 0 and
            input_grid[r+1][0] == input_grid[r][0] and
            input_grid[r+1][1] == input_grid[r][1]):
            legend[input_grid[r][0]] = input_grid[r][1]
            legend_cells.update([(r, 0), (r, 1), (r+1, 0), (r+1, 1)])
            r += 2
        else:
            r += 1

    # Find connected components (shapes) excluding legend cells
    visited = [[False] * cols for _ in range(rows)]
    for rc in legend_cells:
        visited[rc[0]][rc[1]] = True

    shapes: list[tuple[int, set[tuple[int, int]]]] = []
    for r in range(rows):
        for c in range(cols):
            if not visited[r][c] and input_grid[r][c] != 0:
                color = input_grid[r][c]
                component: set[tuple[int, int]] = set()
                queue = deque([(r, c)])
                visited[r][c] = True
                while queue:
                    cr, cc = queue.popleft()
                    component.add((cr, cc))
                    for dr, dc in ((-1,0),(1,0),(0,-1),(0,1)):
                        nr, nc = cr + dr, cc + dc
                        if 0 <= nr < rows and 0 <= nc < cols and not visited[nr][nc] and input_grid[nr][nc] == color:
                            visited[nr][nc] = True
                            queue.append((nr, nc))
                shapes.append((color, component))

    # Add border around each shape
    for color, cells in shapes:
        if color not in legend:
            continue
        border_color = legend[color]

        min_r = min(r for r, _ in cells)
        max_r = max(r for r, _ in cells)
        min_c = min(c for _, c in cells)
        max_c = max(c for _, c in cells)

        # Expanded bounding box (clamped to grid)
        er0 = max(0, min_r - 1)
        er1 = min(rows - 1, max_r + 1)
        ec0 = max(0, min_c - 1)
        ec1 = min(cols - 1, max_c + 1)

        # Find interior holes: 0-cells in bbox not reachable from bbox edge
        zero_cells: set[tuple[int, int]] = set()
        for r in range(min_r, max_r + 1):
            for c in range(min_c, max_c + 1):
                if (r, c) not in cells:
                    zero_cells.add((r, c))

        exterior: set[tuple[int, int]] = set()
        queue = deque()
        for rc in zero_cells:
            if rc[0] in (min_r, max_r) or rc[1] in (min_c, max_c):
                exterior.add(rc)
                queue.append(rc)
        while queue:
            cr, cc = queue.popleft()
            for dr, dc in ((-1,0),(1,0),(0,-1),(0,1)):
                nr, nc = cr + dr, cc + dc
                if (nr, nc) in zero_cells and (nr, nc) not in exterior:
                    exterior.add((nr, nc))
                    queue.append((nr, nc))

        interior = zero_cells - exterior

        # Fill expanded bbox: border_color everywhere except shape cells and interior holes
        for r in range(er0, er1 + 1):
            for c in range(ec0, ec1 + 1):
                if (r, c) in cells:
                    output[r][c] = color
                elif (r, c) in interior:
                    output[r][c] = 0
                else:
                    output[r][c] = border_color

    return output


