"""
ARC puzzle db0c5428 solver.

Rule: The input has a 9x9 pattern (3x3 grid of 3x3 sub-blocks) with a 3x3
background-colored hole in the center. The transform:
1. Keeps the original pattern unchanged.
2. Fills the center hole by reflecting values from adjacent blocks' inner edges.
3. Places 8 copies of the sub-blocks outside the pattern in a 180°-rotated arrangement.
"""
import copy
from collections import Counter


def transform(input_grid: list[list[int]]) -> list[list[int]]:
    rows = len(input_grid)
    cols = len(input_grid[0])

    # Background = most common color
    flat = [c for row in input_grid for c in row]
    bg = Counter(flat).most_common(1)[0][0]

    # Pattern bounding box
    min_r, max_r, min_c, max_c = rows, -1, cols, -1
    for r in range(rows):
        for c in range(cols):
            if input_grid[r][c] != bg:
                min_r, max_r = min(min_r, r), max(max_r, r)
                min_c, max_c = min(min_c, c), max(max_c, c)

    S = (max_r - min_r + 1) // 3  # sub-block size

    # Extract 3x3 grid of sub-blocks
    blocks = {}
    for bi in range(3):
        for bj in range(3):
            blocks[(bi, bj)] = [
                [input_grid[min_r + bi * S + i][min_c + bj * S + j]
                 for j in range(S)]
                for i in range(S)
            ]

    output = copy.deepcopy(input_grid)

    # Dominant pattern color (for center-of-center cell)
    pcols = Counter()
    for r in range(min_r, max_r + 1):
        for c in range(min_c, max_c + 1):
            if input_grid[r][c] != bg:
                pcols[input_grid[r][c]] += 1
    dominant = pcols.most_common(1)[0][0]

    # Fill center hole via reflection from adjacent blocks
    mid = S // 2
    for i in range(S):
        for j in range(S):
            bi = 0 if i < mid else (1 if i == mid else 2)
            si = i if i == mid else S - 1 - i
            bj = 0 if j < mid else (1 if j == mid else 2)
            sj = j if j == mid else S - 1 - j

            val = dominant if (bi == 1 and bj == 1) else blocks[(bi, bj)][si][sj]
            output[min_r + S + i][min_c + S + j] = val

    # Place 8 outside blocks (point-reflected through center)
    outside = [
        (0, 0, min_r - S,     min_c - S),
        (0, 1, min_r - S,     min_c + S),
        (0, 2, min_r - S,     min_c + 3*S),
        (1, 0, min_r + S,     min_c - S),
        (1, 2, min_r + S,     min_c + 3*S),
        (2, 0, min_r + 3*S,   min_c - S),
        (2, 1, min_r + 3*S,   min_c + S),
        (2, 2, min_r + 3*S,   min_c + 3*S),
    ]
    for oi, oj, r0, c0 in outside:
        src = blocks[(2 - oi, 2 - oj)]
        for i in range(S):
            for j in range(S):
                r, c = r0 + i, c0 + j
                if 0 <= r < rows and 0 <= c < cols:
                    output[r][c] = src[i][j]

    return output


