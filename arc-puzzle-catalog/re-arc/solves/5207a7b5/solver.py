def transform(input_grid: list[list[int]]) -> list[list[int]]:
    nrows = len(input_grid)
    ncols = len(input_grid[0])

    # Find gray(5) column and length (vertical line from top)
    col = None
    L = 0
    for r in range(nrows):
        if col is None:
            for c in range(ncols):
                if input_grid[r][c] == 5:
                    col = c
                    L = 1
                    break
        elif input_grid[r][col] == 5:
            L += 1
        else:
            break

    output = [[0] * ncols for _ in range(nrows)]

    for r in range(nrows):
        # Gray line preserved
        if r < L:
            output[r][col] = 5

        # Cyan(8) staircase to the left
        # Full width (=col) for rows 0..L+1, then decreases by 1 every 2 rows
        cw = max(0, col - max(0, (r - L) // 2))
        for c in range(cw):
            output[r][c] = 8

        # Magenta(6) staircase to the right
        # Starts at col-1 wide at row 0, decreases by 1 every 2 rows
        mw = max(0, (L - r - 1) // 2)
        for c in range(col + 1, min(ncols, col + 1 + mw)):
            output[r][c] = 6

    return output


