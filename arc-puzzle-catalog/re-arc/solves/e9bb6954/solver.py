import copy
from collections import deque

def transform(input_grid: list[list[int]]) -> list[list[int]]:
    """
    Rule: Find all 3x3 solid monochromatic blocks. For each block, draw a cross
    (full row + full column through the block's center) in the block's color.
    At intersections of different blocks' cross lines, set cell to 0.
    Preserve the 3x3 blocks themselves.
    """
    rows = len(input_grid)
    cols = len(input_grid[0])
    output = copy.deepcopy(input_grid)

    # Flood-fill to find connected components (4-connectivity)
    visited = [[False] * cols for _ in range(rows)]
    blocks = []  # (center_r, center_c, color, min_r, min_c)

    for r in range(rows):
        for c in range(cols):
            if not visited[r][c] and input_grid[r][c] != 0:
                color = input_grid[r][c]
                q = deque([(r, c)])
                visited[r][c] = True
                cells = []
                while q:
                    cr, cc = q.popleft()
                    cells.append((cr, cc))
                    for dr, dc in ((0,1),(0,-1),(1,0),(-1,0)):
                        nr, nc = cr+dr, cc+dc
                        if 0 <= nr < rows and 0 <= nc < cols and not visited[nr][nc] and input_grid[nr][nc] == color:
                            visited[nr][nc] = True
                            q.append((nr, nc))
                if len(cells) == 9:
                    rs = [r for r,c in cells]
                    cs = [c for r,c in cells]
                    min_r, max_r = min(rs), max(rs)
                    min_c, max_c = min(cs), max(cs)
                    if max_r - min_r == 2 and max_c - min_c == 2:
                        blocks.append((min_r+1, min_c+1, color, min_r, min_c))

    # Draw cross lines
    for cr, cc, color, _, _ in blocks:
        for c in range(cols):
            output[cr][c] = color
        for r in range(rows):
            output[r][cc] = color

    # Zero out intersections of different blocks' cross lines
    for i in range(len(blocks)):
        for j in range(len(blocks)):
            if i != j:
                output[blocks[i][0]][blocks[j][1]] = 0

    # Restore 3x3 block cells
    for _, _, color, min_r, min_c in blocks:
        for dr in range(3):
            for dc in range(3):
                output[min_r+dr][min_c+dc] = color

    return output


