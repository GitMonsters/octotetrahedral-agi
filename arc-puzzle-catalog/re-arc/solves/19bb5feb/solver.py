def transform(input_grid: list[list[int]]) -> list[list[int]]:
    rows = len(input_grid)
    cols = len(input_grid[0])
    
    # Find cyan(8) rectangle bounds
    min_r, max_r, min_c, max_c = rows, 0, cols, 0
    for r in range(rows):
        for c in range(cols):
            if input_grid[r][c] == 8:
                min_r = min(min_r, r)
                max_r = max(max_r, r)
                min_c = min(min_c, c)
                max_c = max(max_c, c)
    
    center_r = (min_r + max_r) / 2.0
    center_c = (min_c + max_c) / 2.0
    
    # Find colored 2x2 blocks (not 0, not 8)
    found = {}
    for r in range(rows):
        for c in range(cols):
            v = input_grid[r][c]
            if v != 0 and v != 8 and v not in found:
                # Record center of this color's block
                cr = sum(rr for rr in range(rows) for cc in range(cols) if input_grid[rr][cc] == v) / 4.0
                cc_val = sum(cc for rr in range(rows) for cc in range(cols) if input_grid[rr][cc] == v) / 4.0
                quad_r = 0 if cr < center_r else 1
                quad_c = 0 if cc_val < center_c else 1
                found[v] = (quad_r, quad_c)
    
    output = [[0, 0], [0, 0]]
    for color, (qr, qc) in found.items():
        output[qr][qc] = color
    return output


