"""ARC puzzle bcb3040b solver.
Rule: Find the two red(2) pixels. Draw a line between them.
Along the line, map 0→2, 1→3 (red pixels stay 2).
"""
import copy

def transform(input_grid: list[list[int]]) -> list[list[int]]:
    grid = copy.deepcopy(input_grid)
    rows, cols = len(grid), len(grid[0])

    # Find the two red(2) cells
    reds = [(r, c) for r in range(rows) for c in range(cols) if grid[r][c] == 2]
    (r1, c1), (r2, c2) = reds[0], reds[1]

    # Walk from (r1,c1) to (r2,c2) using unit steps
    dr = (0 if r2 == r1 else (1 if r2 > r1 else -1))
    dc = (0 if c2 == c1 else (1 if c2 > c1 else -1))

    r, c = r1, c1
    while True:
        if grid[r][c] == 0:
            grid[r][c] = 2
        elif grid[r][c] == 1:
            grid[r][c] = 3
        if r == r2 and c == c2:
            break
        r += dr
        c += dc

    return grid


