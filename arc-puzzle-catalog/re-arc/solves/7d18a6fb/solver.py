def transform(input_grid: list[list[int]]) -> list[list[int]]:
    """
    Rule: A 7x7 blue(1) rectangle acts as a legend with 4 color markers at
    (row+1,col+1), (row+1,col+5), (row+5,col+1), (row+5,col+5).
    Each marker names a color whose shape (found elsewhere in the grid)
    goes into the corresponding quadrant of the 7x7 output.
    """
    rows, cols = len(input_grid), len(input_grid[0])

    # Find the 7x7 blue rectangle
    blue_top = blue_left = -1
    for r in range(rows - 6):
        for c in range(cols - 6):
            ok = True
            for dr in range(7):
                for dc in range(7):
                    if (dr, dc) in {(1,1),(1,5),(5,1),(5,5)}:
                        continue
                    if input_grid[r+dr][c+dc] != 1:
                        ok = False
                        break
                if not ok:
                    break
            if ok:
                blue_top, blue_left = r, c
                break
        if blue_top >= 0:
            break

    # Read the 4 color markers
    tl = input_grid[blue_top+1][blue_left+1]
    tr = input_grid[blue_top+1][blue_left+5]
    bl = input_grid[blue_top+5][blue_left+1]
    br = input_grid[blue_top+5][blue_left+5]

    def extract(color):
        cells = [(r,c) for r in range(rows) for c in range(cols)
                 if input_grid[r][c] == color
                 and not (blue_top <= r <= blue_top+6 and blue_left <= c <= blue_left+6)]
        mr, mc = min(r for r,c in cells), min(c for r,c in cells)
        xr, xc = max(r for r,c in cells), max(c for r,c in cells)
        shape = [[0]*(xc-mc+1) for _ in range(xr-mr+1)]
        for r,c in cells:
            shape[r-mr][c-mc] = color
        return shape

    out = [[0]*7 for _ in range(7)]
    for (qr,qc), color in [((0,0),tl),((0,4),tr),((4,0),bl),((4,4),br)]:
        s = extract(color)
        for r in range(len(s)):
            for c in range(len(s[0])):
                out[qr+r][qc+c] = s[r][c]
    return out


