"""ARC puzzle 54db823b solver.
Rule: The grid contains several rectangular blocks of colored cells (3s and 9s)
separated by black (0) borders. Remove the connected component that has the
fewest 9 (purple/maroon) cells — set all its cells to 0.
"""

from collections import deque
import copy


def transform(input_grid: list[list[int]]) -> list[list[int]]:
    rows = len(input_grid)
    cols = len(input_grid[0])
    grid = copy.deepcopy(input_grid)

    # Find connected components of non-zero cells (4-connectivity)
    visited = [[False] * cols for _ in range(rows)]
    components: list[list[tuple[int, int]]] = []

    for r in range(rows):
        for c in range(cols):
            if input_grid[r][c] != 0 and not visited[r][c]:
                queue = deque([(r, c)])
                visited[r][c] = True
                cells: list[tuple[int, int]] = []
                while queue:
                    cr, cc = queue.popleft()
                    cells.append((cr, cc))
                    for dr, dc in [(-1, 0), (1, 0), (0, -1), (0, 1)]:
                        nr, nc = cr + dr, cc + dc
                        if 0 <= nr < rows and 0 <= nc < cols and not visited[nr][nc] and input_grid[nr][nc] != 0:
                            visited[nr][nc] = True
                            queue.append((nr, nc))
                components.append(cells)

    # Find the component with the fewest 9s and remove it
    min_nines = float('inf')
    min_comp = None
    for comp in components:
        nines = sum(1 for r, c in comp if input_grid[r][c] == 9)
        if nines < min_nines:
            min_nines = nines
            min_comp = comp

    if min_comp is not None:
        for r, c in min_comp:
            grid[r][c] = 0

    return grid


