def transform(input_grid):
    grid = [row[:] for row in input_grid]
    rows = len(grid)
    cols = len(grid[0])

    # Find anchor columns (containing Gray=5)
    anchor_cols = set()
    for r in range(rows):
        for c in range(cols):
            if grid[r][c] == 5:
                anchor_cols.add(c)

    for c in anchor_cols:
        # Collect non-zero pixels in this column
        last_row = {}
        for r in range(rows):
            if input_grid[r][c] != 0:
                last_row[input_grid[r][c]] = r  # last occurrence

        # Sort colors by their last occurrence row
        sorted_colors = sorted(last_row.items(), key=lambda x: x[1])

        # Fill column segments
        prev_end = -1
        for color, last_r in sorted_colors:
            for r in range(prev_end + 1, last_r + 1):
                grid[r][c] = color
            prev_end = last_r

    return grid


