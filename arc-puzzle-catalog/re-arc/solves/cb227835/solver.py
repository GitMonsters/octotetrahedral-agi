def transform(input_grid: list[list[int]]) -> list[list[int]]:
    rows = len(input_grid)
    cols = len(input_grid[0])
    output = [row[:] for row in input_grid]

    # Find the two cyan (8) points
    points = []
    for r in range(rows):
        for c in range(cols):
            if input_grid[r][c] == 8:
                points.append((r, c))

    r1, c1 = points[0]
    r2, c2 = points[1]

    dr = r2 - r1
    dc = c2 - c1
    sr = (1 if dr > 0 else -1) if dr != 0 else 0
    sc = (1 if dc > 0 else -1) if dc != 0 else 0
    abs_dr = abs(dr)
    abs_dc = abs(dc)

    excess = abs(abs_dc - abs_dr)
    diag_steps = min(abs_dr, abs_dc) - 1

    green_cells = set()

    if abs_dc >= abs_dr:
        # Horizontal caps + diagonal
        # Path 1: cap near A, then diagonal toward B
        for k in range(1, excess + 1):
            green_cells.add((r1, c1 + k * sc))
        cap1_r, cap1_c = r1, c1 + excess * sc
        for j in range(1, diag_steps + 1):
            green_cells.add((cap1_r + j * sr, cap1_c + j * sc))

        # Path 2: cap near B, then diagonal toward A
        for k in range(1, excess + 1):
            green_cells.add((r2, c2 - k * sc))
        cap2_r, cap2_c = r2, c2 - excess * sc
        for j in range(1, diag_steps + 1):
            green_cells.add((cap2_r - j * sr, cap2_c - j * sc))
    else:
        # Vertical caps + diagonal
        # Path 1: cap near A, then diagonal toward B
        for k in range(1, excess + 1):
            green_cells.add((r1 + k * sr, c1))
        cap1_r, cap1_c = r1 + excess * sr, c1
        for j in range(1, diag_steps + 1):
            green_cells.add((cap1_r + j * sr, cap1_c + j * sc))

        # Path 2: cap near B, then diagonal toward A
        for k in range(1, excess + 1):
            green_cells.add((r2 - k * sr, c2))
        cap2_r, cap2_c = r2 - excess * sr, c2
        for j in range(1, diag_steps + 1):
            green_cells.add((cap2_r - j * sr, cap2_c - j * sc))

    for r, c in green_cells:
        if 0 <= r < rows and 0 <= c < cols:
            output[r][c] = 3

    return output


