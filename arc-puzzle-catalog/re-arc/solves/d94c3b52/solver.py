"""ARC puzzle d94c3b52 solver.

Rule: The grid is divided into a 4x6 grid of 3x3 blocks (separated by zero-rows/cols).
One block contains color 8 (cyan) — the "seed". Its binary pattern (nonzero vs zero) is
the template. All blocks sharing that binary pattern become cyan (8). For every pair of
cyan blocks in the same row or column of the block grid, all intermediate non-cyan blocks
become orange (7). Remaining blocks stay blue (1). Colors replace the 1s in each block's
pattern; 0s stay 0.
"""

def transform(input_grid: list[list[int]]) -> list[list[int]]:
    rows = len(input_grid)
    cols = len(input_grid[0])

    # Find separator rows and columns (all zeros)
    sep_rows = [r for r in range(rows) if all(v == 0 for v in input_grid[r])]
    sep_cols = [c for c in range(cols) if all(input_grid[r][c] == 0 for r in range(rows))]

    # Extract block bands
    row_bands = []
    for i in range(len(sep_rows) - 1):
        r0 = sep_rows[i] + 1
        r1 = sep_rows[i + 1]
        if r1 > r0:
            row_bands.append((r0, r1))

    col_bands = []
    for i in range(len(sep_cols) - 1):
        c0 = sep_cols[i] + 1
        c1 = sep_cols[i + 1]
        if c1 > c0:
            col_bands.append((c0, c1))

    n_br = len(row_bands)
    n_bc = len(col_bands)

    # Extract each block's binary pattern and find the cyan seed
    def get_block(br, bc):
        r0, r1 = row_bands[br]
        c0, c1 = col_bands[bc]
        return [[input_grid[r][c] for c in range(c0, c1)] for r in range(r0, r1)]

    def to_binary(block):
        return tuple(tuple(1 if v != 0 else 0 for v in row) for row in block)

    seed_br, seed_bc = -1, -1
    seed_pattern = None
    for br in range(n_br):
        for bc in range(n_bc):
            blk = get_block(br, bc)
            if any(v == 8 for row in blk for v in row):
                seed_br, seed_bc = br, bc
                seed_pattern = to_binary(blk)
                break
        if seed_pattern:
            break

    # Find all blocks matching the seed pattern -> cyan
    is_cyan = [[False] * n_bc for _ in range(n_br)]
    block_patterns = [[None] * n_bc for _ in range(n_br)]
    for br in range(n_br):
        for bc in range(n_bc):
            bp = to_binary(get_block(br, bc))
            block_patterns[br][bc] = bp
            if bp == seed_pattern:
                is_cyan[br][bc] = True

    # Find orange blocks: between two cyan blocks in same row or column
    is_orange = [[False] * n_bc for _ in range(n_br)]

    # Check rows
    for br in range(n_br):
        cyan_cols = sorted(bc for bc in range(n_bc) if is_cyan[br][bc])
        for i in range(len(cyan_cols) - 1):
            for bc in range(cyan_cols[i] + 1, cyan_cols[i + 1]):
                if not is_cyan[br][bc]:
                    is_orange[br][bc] = True

    # Check columns
    for bc in range(n_bc):
        cyan_rows = sorted(br for br in range(n_br) if is_cyan[br][bc])
        for i in range(len(cyan_rows) - 1):
            for br in range(cyan_rows[i] + 1, cyan_rows[i + 1]):
                if not is_cyan[br][bc]:
                    is_orange[br][bc] = True

    # Build output grid
    output = [row[:] for row in input_grid]
    for br in range(n_br):
        for bc in range(n_bc):
            r0, r1 = row_bands[br]
            c0, c1 = col_bands[bc]
            if is_cyan[br][bc]:
                color = 8
            elif is_orange[br][bc]:
                color = 7
            else:
                color = 1  # stays blue
            for r in range(r0, r1):
                for c in range(c0, c1):
                    if input_grid[r][c] != 0:
                        output[r][c] = color
    return output


